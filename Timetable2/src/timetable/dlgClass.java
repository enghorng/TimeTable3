    public void showdate(){               
        try {
            clsCon.setRs(clsCon.getStmt().executeQuery("select ClassName from tblClass"));
            if(clsCon.getRs().first()){  
                do{
                    String name = clsCon.getRs().getString("ClassName"); 
                    lstClass.setModel(modeLst);
                    modeLst.addElement(name);                    
                }while(clsCon.getRs().next());
            }
            
        } catch (Exception e) {
        }
    }
