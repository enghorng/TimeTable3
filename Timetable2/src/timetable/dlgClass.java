    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String sql = "insert into tblClass(ClassID,ClassName,Auto) values('"+ txtClassCode.getText() +"','"+ txtClassName.getText() +"','"+i+"')";
        if(txtClassName.getText().equals("")){
                txtClassName.requestFocusInWindow();
                lblAlert1.setText("Please Fill");
                lblAlert1.setForeground(Color.red);
                lblAlert1.setVisible(true);
                return;
            }
         try{            
                clsCon.getStmt().executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "A new record was saved");
                lstClass.setModel(modeLst);
                modeLst.addElement(txtClassName.getText());
                clear();  
                txtClassName.requestFocus();
                getIdAuto();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "This proccess is fail.");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int answer = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Delete", JOptionPane.YES_NO_OPTION);
        String sql = "delete from tblClass where ClassID='"+ txtClassCode.getText() +"'";
        
        if(txtClassName.getText().equals("")){
                txtClassName.requestFocusInWindow();
                lblAlert1.setText("Please Fill");
                lblAlert1.setForeground(Color.red);
                return;
            }
        if(answer==JOptionPane.YES_OPTION)        
            try{            
                   clsCon.getStmt().executeUpdate(sql);
                   JOptionPane.showMessageDialog(this, "Delete is sueecssful");
                   modeLst.removeElementAt(index);
                   clear();      
           }
           catch(Exception e){
               JOptionPane.showMessageDialog(this, "This proccess is fail.");
           }     
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(lstClass.getSelectedIndex()<0)
            return;
        String sql = "update tblClass set ClassName='"+ txtClassName.getText() +"' where ClassID='"+ txtClassCode.getText() +"'";
        if(txtClassName.getText().equals("")){
                txtClassName.requestFocusInWindow();
                lblAlert1.setText("Please Fill");
                lblAlert1.setForeground(Color.red);
                return;
            }
         try{            
                clsCon.getStmt().executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Update is sueecssful");
                lstClass.setModel(modeLst);
                modeLst.setElementAt(txtClassName.getText(),index);
                clear();      
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "This proccess is fail.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

