    void viewEmp(){
        DefaultTableModel modeTbl;
        modeTbl = (DefaultTableModel)jtblEmp.getModel();
       
        try {
            clsCon.setRs(clsCon.getStmt().executeQuery("select * from tblEmployee"));
            if(clsCon.getRs().first()){  
                do{
                    String id = clsCon.getRs().getString("EmpID");
                    String name = clsCon.getRs().getString("Name");
                    String gender = clsCon.getRs().getString("Gender");
                    String dob = clsCon.getRs().getString("DOB");
                    String phone = clsCon.getRs().getString("Phone");
                    String st[]={id,name,gender,dob,phone};
                    
                    modeTbl.addRow(st);
                }while(clsCon.getRs().next());
            }
        } catch (Exception e) {
        }
    }
