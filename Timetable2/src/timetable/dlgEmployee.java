    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(jtblEmp.getSelectedRow() == -1)
        return;
        if(JOptionPane.showConfirmDialog(this, "Do you want to delete?","Comfire",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            try{
                int index[] = jtblEmp.getSelectedRows();
                clsCon.getStmt().execute("delete from tblEmployee where EmpID ='"+txtID.getText()+"'");
                JOptionPane.showMessageDialog(this, "Delete is successful.");
                clear();
                txtID.setText("");                

                DefaultTableModel modtable = (DefaultTableModel)jtblEmp.getModel();
                for(int i=0; i<index.length; i++)
                modtable.removeRow(index[i]-i);
                
                if(jtblEmp.getRowCount()==0){
                    btnAdd.setEnabled(true);
                    btnDelete.setEnabled(false);
                    btnUpdate.setEnabled(false);
                    getIdAuto();
                }
            }catch(Exception e){

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = jtblEmp.getSelectedRow();
        if(index==-1)
        return;
        checkEmpName();
        if(lblAlert1.getText().equals("Please Fill"))
        {
            txtName.requestFocus();
            return;
        }
        String  sql="update tblEmployee set Name='"+txtName.getText()+"', Gender='"+cboGender.getSelectedItem()+"', DOB='"+txtDOB.getText()+"', Phone='"+txtPhone.getText()+"'where EmpID='"+txtID.getText()+"'";
        try {
            clsCon.getStmt().executeUpdate(sql);
            JOptionPane.showMessageDialog(this, "Update is successful");
            jtblEmp.setValueAt(txtName.getText(), index, 1);
            jtblEmp.setValueAt(cboGender.getSelectedItem(), index, 2);
            jtblEmp.setValueAt(txtDOB.getText(), index, 3);
            jtblEmp.setValueAt(txtPhone.getText(), index, 4);
            //            clear();
            //            txtID.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Cannot Update.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

